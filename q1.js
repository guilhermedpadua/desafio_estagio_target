let INDICE = 13;
let SOMA = 0;
let K = 0;

while (K < INDICE) {
    K = K + 1;
    SOMA = SOMA + K;
}

console.log(SOMA); // O valor de SOMA será 91

// O código utiliza um laço while que incrementa a variável K e soma seu valor à variável SOMA em cada iteração. No final do processamento, o valor de SOMA será 91